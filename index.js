class Character {
    constructor(){
        name = "";
        alignment = "";
    }



    getName () {
        return this.name;
    }

    setName (newName) {
        this.name = newName;
    }
    
    getAlignment () {
        return this.name;
    }

    setAligntment (newAlignment) {
        this.alignment = newAlignment;
    }
}

export default Character